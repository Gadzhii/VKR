<?php
session_start();

// Проверяем, был ли пользователь авторизован
if (isset($_SESSION['loggedin']) && $_SESSION['loggedin'] === true) {
    // Пользователь авторизован, выполняем действие (например, изменение баланса)
    $amount = $_POST['amount']; // сумма для изменения баланса
    $account = $_POST['account']; // номер аккаунта для изменения

    // Имитация изменения баланса
    echo "Баланс аккаунта $account изменен на $amount";
} else {
    // Пользователь не авторизован, выводим сообщение об ошибке
    echo "Ошибка: пользователь не авторизован!";
}
?>
