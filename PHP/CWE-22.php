<?php
// Получаем имя файла из параметра запроса
$file = $_GET['file'];

// Путь к файлам на сервере
$directory = '/var/www/files/';

// Полный путь к запрашиваемому файлу
$path = $directory . $file;

// Проверяем, существует ли файл
if (file_exists($path)) {
    // Если файл существует, выводим его содержимое
    echo file_get_contents($path);
} else {
    // Если файл не существует, выводим сообщение об ошибке
    echo 'Файл не найден.';
}
?>
